  import YShelf from '/components/shelf'
  import YButton from '/components/YButton'
  import YPopup from '/components/popup'
  import YHeader from '/common/header'
  import YFooter from '/common/footer'
  import {mapGetters, mapState} from 'vuex'
  import {Loading} from 'element-ui'
  import {submitOrder} from '../../api/orders'
  import {delFromCart} from '../../api/cart'
  export default {
    data () {
      return {
        fullscreenLoading: false,
        basicFoodUrl: require('../../../static/images/coffeeicon.png'),
        currentOrder: [],
        addList: null,
        addressId: '1',
        popupOpen: false,
        popupTitle: '管理收货地址',
        msg: {
          addressId: '',
          userName: '',
          tel: '',
          streetName: '',
          isDefault: false
        }
      }
    },
    computed: {
      ...mapState(['cartList']),
      ...mapGetters(['name']),
      btnHighlight () {
        let msg = this.msg
        return msg.userName && msg.tel && msg.streetName
      },
      // 计算选中的商品的总价格
      checkPrice () {
        let totalPrice = 0
        this.currentOrder && this.currentOrder.forEach(item => {
          totalPrice += (item.item_count * item.price)
        })
        return totalPrice
      }
    },
    methods: {
      // 提取当前购物车
      getOrderList () {
        let cart = this.cartList
        cart.forEach(item => {
          if (item.checked === true) {
            this.currentOrder.push(item)
          }
        })
        if (cart.length === 0) {
          // 当前没有任何商品要被购买，请返回购物车！
          this.$router.push({ path: 'cart' })
        }
      },
      // TODO - 提取当前用户所具有的地址
      getAddressList () {
        let res = {
          status: '0',
          msg: 'suc',
          result: [
            {
              addressId: 1526562111000,
              userName: '李涵',
              tel: '2558 3888',
              streetName: '香港半山區干德道36號慧明苑A-23/F',
              isDefault: true,
              _id: '5afd7d3fd4e2770fa6751b4e'
            },
            {
              addressId: 1526562111020,
              userName: '李小野',
              tel: '139 0600 8040',
              streetName: '中国广西壮族自治区南宁市青秀区东葛路29-1号8-16/F',
              isDefault: false,
              _id: '5afd7d3fd4e2770fa6751b4f'
            }
          ]
        }
        let data = res.result
        if (data.length) {
          this.addList = data
          this.addressId = data[0].addressId || '1'
        } else {
          this.addList = []
        }
      },
      // TODO - 更新当前用户具有的地址
      addressUpdate (params) {
        this.getAddressList()
      },
      // TODO - 添加地址
      addressAdd (params) {
        this.getAddressList()
      },
      // TODO - 删掉地址
      addressDel (params) {
        this.getAddressList()
      },
      // TODO - 选择地址
      selectAddress (id) {
        this.addressId = id
      },
      // TODO - 去付款
      pay () {
        // 先创建订单
        let loadingInstance = Loading.service({ fullscreen: true })
        let orderInfo = {
          items: this.currentOrder,
          customer_name: this.name
        }
        // 然后提交给服务器
        submitOrder(orderInfo).then(result => {
          // 清除购物车
          this.currentOrder.forEach(item => {
            delFromCart({
              item_id: item.item_id
            })
          })
          // 关闭loading
          setTimeout(() => {
            loadingInstance.close()
            // 再前往订单成功并支付页面
            this.$router.push({
              name: 'payment',
              params: {
                addressId: this.addressId,
                price: this.checkPrice,
                hasErr: result.message !== 'NO ERR'
              }
            })
          }, 2000)
        })
      },
      // TODO - 修改某个地址？
      updateAddr (item) {
        this.popupOpen = true
        if (item) {
          this.popupTitle = '管理收货地址'
          this.msg.userName = item.userName
          this.msg.tel = item.tel
          this.msg.streetName = item.streetName
          this.msg.isDefault = item.isDefault
          this.msg.addressId = item.addressId
        } else {
          this.popupTitle = '新增收货地址'
          this.msg.userName = ''
          this.msg.tel = ''
          this.msg.streetName = ''
          this.msg.isDefault = false
          this.msg.addressId = ''
        }
      },
      // 保存某个地址
      saveAddr (p) {
        // if (p.addressId) {
        //   this.addressUpdate(p)
        // } else {
        //   delete p.addressId
        //   this.addressAdd(p)
        // }
        this.$message('该功能正在开发中！')
        this.popupOpen = false
      },
      // TODO - 删除某个地址
      delAddr (addressId) {
        this.addressDel({addressId})
      }
    },
    created () {
      this.getOrderList()
      this.getAddressList()
    },
    components: {
      YShelf,
      YButton,
      YPopup,
      YHeader,
      YFooter
    }
  }
</script>
